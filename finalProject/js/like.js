document.querySelector(".displayLike").addEventListener("click", toggleLike);

function toggleLike() {
  let toHide = document.querySelector(".displayLike");
  let toShow = document.querySelector(".hiddenLike");
  toShow.classList.remove("hiddenLike");
  toShow.classList.add("displayLike");
  toHide.classList.add("hiddenLike");
  toHide.classList.remove("displayLike");
  
}